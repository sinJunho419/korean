-- ── 테이블 ──
CREATE TABLE IF NOT EXISTS korean_idioms (
  id SERIAL PRIMARY KEY,
  idiom VARCHAR(10) NOT NULL,
  hanja VARCHAR(20),
  meaning TEXT NOT NULL,
  char_meanings JSONB DEFAULT '{}',
  level VARCHAR(20) NOT NULL DEFAULT 'beginner',
  set_no INTEGER NOT NULL DEFAULT 1,
  example_sentence TEXT,
  origin TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_korean_idioms_level ON korean_idioms(level);
CREATE INDEX IF NOT EXISTS idx_korean_idioms_level_set ON korean_idioms(level, set_no);

-- ── 오답 기록 ──
CREATE TABLE IF NOT EXISTS korean_wrong_answers (
  id SERIAL PRIMARY KEY,
  user_id INTEGER NOT NULL,
  idiom_id INTEGER NOT NULL REFERENCES korean_idioms(id),
  wrong_count INTEGER DEFAULT 1,
  consecutive_correct INTEGER DEFAULT 0,
  status VARCHAR(20) DEFAULT 'Learning',
  last_wrong_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(user_id, idiom_id)
);

CREATE INDEX IF NOT EXISTS idx_korean_wrong_user ON korean_wrong_answers(user_id);

-- ── 활동 로그 ──
CREATE TABLE IF NOT EXISTS korean_activity_log (
  id SERIAL PRIMARY KEY,
  user_id INTEGER NOT NULL,
  activity_type VARCHAR(30) NOT NULL,
  level VARCHAR(20),
  set_no INTEGER,
  score INTEGER,
  total INTEGER,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- ── RPC: 오답 기록 ──
CREATE OR REPLACE FUNCTION record_korean_wrong_words(
  p_user_id INTEGER,
  p_idiom_ids INTEGER[]
) RETURNS VOID AS $$
BEGIN
  INSERT INTO korean_wrong_answers (user_id, idiom_id, wrong_count, consecutive_correct, status, last_wrong_at)
  SELECT p_user_id, unnest(p_idiom_ids), 1, 0, 'Learning', NOW()
  ON CONFLICT (user_id, idiom_id)
  DO UPDATE SET
    wrong_count = korean_wrong_answers.wrong_count + 1,
    consecutive_correct = 0,
    status = 'Learning',
    last_wrong_at = NOW();
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- ── RPC: 리벤지 정답 ──
CREATE OR REPLACE FUNCTION record_korean_revenge_correct(
  p_user_id INTEGER,
  p_idiom_id INTEGER
) RETURNS TEXT AS $$
DECLARE
  v_new_count INTEGER;
BEGIN
  UPDATE korean_wrong_answers
  SET consecutive_correct = consecutive_correct + 1,
      last_wrong_at = NOW()
  WHERE user_id = p_user_id AND idiom_id = p_idiom_id;

  SELECT consecutive_correct INTO v_new_count
  FROM korean_wrong_answers
  WHERE user_id = p_user_id AND idiom_id = p_idiom_id;

  IF v_new_count >= 3 THEN
    UPDATE korean_wrong_answers
    SET status = 'Mastered'
    WHERE user_id = p_user_id AND idiom_id = p_idiom_id;
    RETURN 'Mastered';
  END IF;

  RETURN 'Learning';
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- ── RPC: 리벤지 오답 ──
CREATE OR REPLACE FUNCTION record_korean_revenge_wrong(
  p_user_id INTEGER,
  p_idiom_id INTEGER
) RETURNS VOID AS $$
BEGIN
  UPDATE korean_wrong_answers
  SET consecutive_correct = 0,
      last_wrong_at = NOW()
  WHERE user_id = p_user_id AND idiom_id = p_idiom_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;


